function hideshow(){
    var lab=document.getElementById('lab2');
    var q2=document.getElementById('q2');
    var lab_2=document.getElementById('lab_2');
    var a2=document.getElementById('a2');
    var btnfrm=document.getElementById('ocultar2');
    if(btnfrm.style.display=="block"){
        btnfrm.style.display="none"
        document.getElementById('ocultar').value='+';
        lab.style.display="none"
        q2.style.display="none"
        lab_2.style.display="none"
        a2.style.display="none"
    }
    else
    if(btnfrm.style.display=="none"){
        btnfrm.style.display="block"
        document.getElementById('ocultar').value='-';
        lab.style.display="block"
        q2.style.display="block"
        lab_2.style.display="block"
        a2.style.display="block"
        btn_q1.style.display="block"
    }
}


